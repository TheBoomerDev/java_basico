package test.ejercicios;

public class ej03 {
	
	public static void main(String[] args) {
		
		// 0-100
		
		// FOR
		for (int i = 0; i <= 100; i++) {
			System.out.println(i);
		}
		
		// WHILE
		int varWhile = 0;
		while(varWhile <= 100) {
			System.out.println(varWhile);
			varWhile++;
		}
		
		
		// DO- WHILE
		int varDoWhile = 0;
		do {
			System.out.println(varDoWhile);
			varDoWhile++;
		}while(varDoWhile <= 100);
		
	}

}
