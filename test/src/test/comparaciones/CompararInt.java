package test.comparaciones;

public class CompararInt {

	public static void main(String[] args) {

		int valor1 = 0;
		int valor2 = 124;
		
		// Si son iguales
		boolean iguales = valor1 == valor2;
		
		// Si son diferentes
		boolean diferentes = valor1 != valor2;
		
		// Si 1 mayor a otro
		boolean mayorQue = valor1 > valor2;
		
		// Si 1 mayor o igual a
		boolean mayorIgual = valor1 >= valor2;
		
		// Si 2 menor o igual a
		boolean menorIgual = valor1 <= valor2; 

	}

}
