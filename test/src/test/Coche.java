package test;

public class Coche {
	
	private final String marca;
	private int puertas = 3;
	
	private Motor motor;

	public Coche(String marca, int puertas) {
		this.marca = marca;
		this.puertas = puertas;
		
		this.motor = new Motor();
	}
	
	public int getRuedas() {
		return this.puertas;
	}
	
	public Motor getMotor() {
		return this.motor;
	}
}
