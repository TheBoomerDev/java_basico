package snippets.visibilidad;

public class VariableStatic {
	
	
	/**
	 * Puede tener diferentes valores en los diferentes objetos de VariableStatic
	 */
	public int enteroInstancia = 0;
	
	/**
	 * TODOS los objetos de tipo VariableStatic van a tener el mismo valor.
	 */
	public static int enteroClase = 0;

}
